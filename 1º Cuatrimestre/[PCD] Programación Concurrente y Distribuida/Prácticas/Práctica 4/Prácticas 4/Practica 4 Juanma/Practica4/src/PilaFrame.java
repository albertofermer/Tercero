        Pilalenta p = new Pilalenta(CAPACIDAD,canvas);
        
        Productor[] h1 = new Productor[4];
        Thread Consumidor = new Thread(new Consumidor(p));
        
        for (int i = 0; i < 4; i++) {
            h1[i] = new Productor(p);
            h1[i].start();
        }

        Consumidor.start();
        
        Consumidor.join();
        
        for (int i = 0; i < 4; i++) {
            h1[i].join();
        }
               
        Thread.sleep(3000);
        System.exit(0);
